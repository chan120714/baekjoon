q=10**9+7
def pow(n):
    if n==1:
        return[[4,-1],[1,0]]
    if n%2==0:
        x=pow(n//2)
        y=[[0,0],[0,0]]
        y[0][0]=(x[0][0]*x[0][0]+x[0][1]*x[1][0])%q
        y[0][1]=(x[0][0]*x[0][1]+x[0][1]*x[1][1])%-q
        y[1][0]=(x[1][0]*x[0][0]+x[1][1]*x[1][0])%q
        y[1][1]=(x[1][0]*x[0][1]+x[1][1]*x[1][1])%-q
        return y
    if n%2==1:
        x=pow(n//2)
        y=[[0,0],[0,0]]
        p=[[4,-1],[1,0]]
        z=[[0,0],[0,0]]
        y[0][0]=(x[0][0]*x[0][0]+x[0][1]*x[1][0])%q
        y[0][1]=(x[0][0]*x[0][1]+x[0][1]*x[1][1])%-q
        y[1][0]=(x[1][0]*x[0][0]+x[1][1]*x[1][0])%q
        y[1][1]=(x[1][0]*x[0][1]+x[1][1]*x[1][1])%-q
        z[0][0]=(y[0][0]*p[0][0]+y[0][1]*p[1][0])%q
        z[0][1]=(y[0][0]*p[0][1]+y[0][1]*p[1][1])%-q
        z[1][0]=(y[1][0]*p[0][0]+y[1][1]*p[1][0])%q
        z[1][1]=(y[1][0]*p[0][1]+y[1][1]*p[1][1])%-q
        return z
