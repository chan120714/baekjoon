    def pow(n):
        if n==-1:
            return [[0,0],[0,0]]
        if n==0:
            return [[1,1],[1,0]]
        if n==1:
            return[[1,1],[1,0]]
        if n%2==0:
            x=pow(n//2)
            y=[[0,0],[0,0]]
            y[0][0]=(x[0][0]*x[0][0]+x[0][1]*x[1][0])%10000
            y[0][1]=(x[0][0]*x[0][1]+x[0][1]*x[1][1])%10000
            y[1][0]=(x[1][0]*x[0][0]+x[1][1]*x[1][0])%10000
            y[1][1]=(x[1][0]*x[0][1]+x[1][1]*x[1][1])%10000
            return y
        if n%2==1:
            x=pow(n//2)
            y=[[0,0],[0,0]]
            y[0][0]=(x[0][0]*x[0][0]+x[0][1]*x[1][0])%10000
            y[0][1]=(x[0][0]*x[0][1]+x[0][1]*x[1][1])%10000
            y[1][0]=(x[1][0]*x[0][0]+x[1][1]*x[1][0])%10000
            y[1][1]=(x[1][0]*x[0][1]+x[1][1]*x[1][1])%10000
            x[0][0]=(y[0][0]+y[0][1])%10000
            x[0][1]=y[0][0]
            x[1][0]=(y[1][0]+y[1][1])%10000
            x[1][1]=y[1][0]
            return x
    print(pow(a-1)[0][0])